import request from "supertest";
import app from "../app";

//signup, signin and return cookie
const signin = async () => {
  await request(app)
    .post("/api/users/signup")
    .send({
      email: "test@test.com",
      password: "password",
    })
    .expect(201);

  const response = await request(app)
    .post("/api/users/signin")
    .send({
      email: "test@test.com",
      password: "password",
    })
    .expect(200);
  return response.get("Set-Cookie");
};

export default signin;
